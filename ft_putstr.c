#include "ft_printf.h"

void	ft_putstr(char *s, int *cp)
{
	int	i;

	i = 0;
	if (!s)
	{
		*cp += write(1, "(null)", 6);
		return ;
	}
	while (s[i])
		*cp += write(1, &s[i++], 1);
}
